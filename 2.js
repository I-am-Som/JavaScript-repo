// operators

// Arithmatic operator-----------------------------------

let x = 5;
let y = 2;

console.log(x + y); // Addition: 7
console.log(x - y); // Subtraction: 3
console.log(x * y); // Multiplication: 10
console.log(x / y); // Division: 2.5
console.log(x % y); // Modulus: 1
console.log(x ** y); // Exponentiation: 25

// logical operator---------------------------------------

let x1 = true;
let y1 = false;

console.log(x1 && y1); // AND: false
console.log(x1 || y1); // OR: true
console.log(!x1); // NOT: false

// comparison operator-----------------------------------

let x2 = 5;
let y2 = 5;

console.log(x2 == y2); // Equal: true
console.log(x2 === y2); // Strict Equal: true
console.log(x2 > y2); // Greater Than: false
console.log(x2 >= y2); // Greater Than or Equal: true

// Assignment Operators-----------------------------------

let x3 = 5;
let y3 = 2;

x3 += y3; // x = 7
x3 -= y3; // x = 5
x3 *= y3; // x = 10
x3 /= y3; // x = 5
x3 %= y3; // x = 1
x3 **= y3; // x = 25